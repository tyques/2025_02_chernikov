package ru.shift.common;

/**
 * Типы сообщений, используемые в протоколе обмена данными.
 * Определяет назначение передаваемого пакета данных между клиентом и сервером.
 */
public enum MessageType {
    /**
     * Запрос на вход клиента.
     * В качестве полезной нагрузки (payload) ожидается имя пользователя (username).
     */
    LOGIN_REQUEST,

    /**
     * Успешный вход.
     * Сервер подтверждает авторизацию и разрешает доступ.
     */
    LOGIN_SUCCESS,

    /**
     * Ошибка входа.
     * Отправляется, если имя пользователя уже занято или произошла иная ошибка валидации.
     */
    LOGIN_ERROR,

    /**
     * Обычное сообщение чата.
     * Текст, отправленный пользователем для других участников.
     */
    CHAT_MESSAGE,

    /**
     * Системное сообщение.
     * Информационное сообщение от сервера (например, о входе или выходе участников).
     */
    SYSTEM_MESSAGE,

    /**
     * Обновление списка пользователей.
     * Отправляется клиентам для актуализации списка активных участников чата.
     */
    USER_LIST_UPDATE,

    /**
     * Запрос истории сообщений.
     * В качестве полезной нагрузки (payload) может содержать индекс или параметры пагинации.
     */
    HISTORY_REQUEST,

    /**
     * Ответ с историей сообщений.
     * Содержит запрошенный список сообщений из архива чата.
     */
    HISTORY_RESPONSE
}